package com.test.java;

public class Ex10 {
	
	public static void main(String[] args) {
		
		int javaScore = 83;
		int pythonScore = 100;
		
		System.out.println("두 시험 점수의 합: " + javaScore+pythonScore);
		System.out.println("두 시험 점수의 합: " + (javaScore+pythonScore));
		
		
		int score1 = 10;
		int score2 = 3;
		
		System.out.println("+ 연산 결과 : " + (score1+score2));
		System.out.println("- 연산 결과 : " + (score1-score2));
		System.out.println("* 연산 결과 : " + (score1*score2));
		System.out.println("/ 연산 결과 : " + (score1/score2));
		System.out.println("% 연산 결과 : " + (score1%score2));
	
	
	}

}
