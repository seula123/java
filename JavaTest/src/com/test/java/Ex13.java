package com.test.java;

public class Ex13 {
	
	public static void main(String[] args) {
		
		int score1 = 10;
		int score2 = 3;
		
		System.out.println(score1>score2);  //true
		System.out.println(score1>=score2); //true
		System.out.println(score1<score2);  //false
		System.out.println(score1<=score2); //false
		System.out.println(score1==score2); //false
		System.out.println(score1!=score2); //true
	}
}
